# Crie um programa que tenha uma função lambda que receba um número e adicione o valor
# 15 a esse número. O programa deve imprimir o resultado da função.

# Função lambda que adiciona 15 ao número fornecido
adicionar_15 = lambda x: x + 15

# Imprimindo o resultado da função lambda com um exemplo
print(adicionar_15(10))  # Exemplo com o número 10 + 15 = 25
