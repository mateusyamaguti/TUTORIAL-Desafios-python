# Problema 1003 - Soma Simples
# Fonte: https://judge.beecrowd.com/

# O problema pede para ler dois números inteiros e mostrar a soma entre eles.
# A saída deve estar exatamente no formato "SOMA = X".

# -----------------------------------------------------

# Lemos o primeiro número inteiro
A = int(input())

# Lemos o segundo número inteiro
B = int(input())

# Calculamos a soma
SOMA = A + B

# Exibimos o resultado formatado exatamente como o enunciado pede
print(f"SOMA = {SOMA}")
