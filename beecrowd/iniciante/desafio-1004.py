# Problema 1004 - Produto Simples
# Fonte: https://judge.beecrowd.com/

# O problema pede para ler dois números inteiros,
# calcular o produto entre eles e exibir o resultado
# no formato: PROD = X

# -----------------------------------------------------

# Lemos o primeiro número inteiro
A = int(input())

# Lemos o segundo número inteiro
B = int(input())

# Calculamos o produto dos dois valores
PROD = A * B

# Exibimos o resultado exatamente no formato pedido
print(f"PROD = {PROD}")
